			end loop;
			UPDATE CXRJ.DOC_BORDERÔ_P SET DOC_BORDERÔ_P.VALOR = VAL_TOTAL WHERE DOC_BORDERÔ_P.EMB_REF = EMB_REF_ORIG and DOC_BORDERÔ_P.EMB_DOC_SEQ = EMB_SEQ_ORIG;			
		ELSIF TIPO_DOC = 'DOC_SAQUE_2V' THEN
